#include "libft.h"

long	ft_atoi(const char *str)
{
	int		i;
	int		s;
	long	rsl;

	(1) && (i = 0, s = 1, rsl = 0);
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == ' ')
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			s *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		if (((rsl * 10) + (str[i] - 48) * s > 2147483647
				|| (rsl * 10) + (str[i] - 48) * s < -2147483648))
			return (-2147483649);
		rsl = (rsl * 10) + (str[i] - 48);
		i++;
	}
	return (rsl * s);
}
